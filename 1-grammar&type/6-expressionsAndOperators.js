// assignment operator
let  x = 5;

// Addition assignment
x += 1; // x =x +1;


//Subtraction Assignment
x -= 1; // x = x - 1

//Multiplication assignment
x *= 1; // x = x * 1

//Divition assignment
x /= 1; // x = x / 1

// Remainder (or Modulus) assignment
x %= 1; // x = x % 1

//Exponential assignment
x **= 1; // x = x ** 1

